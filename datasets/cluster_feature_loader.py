# coding=utf-8
"""
Cluster feature LMDB loader for records generated by run/03_extract_cell_cluster_indices.py.

LMDB record format (msgpack):
- L: int
- g: bytes, uint16[L]      # scGPT gene ids
- c: bytes, uint8[L]       # per-gene cluster index
- s: bytes, float16[768]   # cls token

This module provides:
1) O(1) random read by (db_name, key)
2) Optional multiprocessing batch read
3) Codebook lookup utilities:
   - map (g, c) -> 768-d features
   - build model-compatible gene embedding init matrix
"""

from __future__ import annotations

import json
import multiprocessing as mp
from dataclasses import dataclass
from pathlib import Path
from typing import Dict, List, Optional, Sequence, Tuple, Union

import lmdb
import msgpack
import numpy as np
import torch

KeyType = Union[bytes, str]
DbType = Union[str, Path]


@dataclass
class ClusterFeatureRecord:
    key: bytes
    length: int
    scgpt_ids: np.ndarray       # uint16 [L]
    cluster_indices: np.ndarray # uint8 [L]
    cls_token: np.ndarray       # float16 [D], usually D=768


@dataclass
class CodebookLookupResult:
    features_768: np.ndarray    # float16 [L, 768]
    valid_mask: np.ndarray      # bool [L]
    token_ids: Optional[np.ndarray] = None  # int64 [L], local_id*clusters_per_gene + cid; unknown=-1


def _normalize_db_name(db_name: DbType) -> str:
    s = Path(str(db_name)).name
    if s.endswith('_cluster.db'):
        return s
    if s.endswith('.db'):
        return s[:-3] + '_cluster.db'
    return s + '_cluster.db'


def _normalize_key(key: KeyType) -> bytes:
    if isinstance(key, bytes):
        return key
    return str(key).encode('utf-8')


def _decode_record(key: bytes, raw: bytes) -> ClusterFeatureRecord:
    rec = msgpack.unpackb(raw, raw=False)
    L = int(rec.get('L', 0))

    g = np.frombuffer(rec['g'], dtype=np.uint16, count=L if L > 0 else -1).copy()
    c = np.frombuffer(rec['c'], dtype=np.uint8, count=L if L > 0 else -1).copy()
    s = np.frombuffer(rec['s'], dtype=np.float16).copy()

    if L <= 0:
        L = int(min(len(g), len(c)))
        g = g[:L]
        c = c[:L]

    return ClusterFeatureRecord(
        key=key,
        length=L,
        scgpt_ids=g,
        cluster_indices=c,
        cls_token=s,
    )


class GeneCodebookLookup:
    """
    Codebook lookup helper.

    Supports both:
    - compact artifacts: codebook_tensor_compact.pt + gene_to_compact_idx.json + gene_nclusters.json
    - full artifacts:    codebook_tensor.pt + gene_nclusters.json

    The local-id ordering matches modeling_gene_transformer_rank_pe.py:
    valid_genes = sorted([gid for gid, k in nclusters.items() if k > 0])
    token_id = local_id * clusters_per_gene + cluster_idx
    """

    def __init__(
        self,
        codebook_dir: Union[str, Path],
        clusters_per_gene: int = 64,
        prefer_compact: bool = True,
    ):
        self.codebook_dir = Path(codebook_dir)
        self.clusters_per_gene = int(clusters_per_gene)

        nclusters_path = self.codebook_dir / 'gene_nclusters.json'
        if not nclusters_path.exists():
            raise FileNotFoundError(f'missing {nclusters_path}')
        with open(nclusters_path, 'r') as f:
            self.nclusters: Dict[int, int] = {int(k): int(v) for k, v in json.load(f).items()}

        self.valid_genes: List[int] = sorted([gid for gid, k in self.nclusters.items() if k > 0])
        self.scgpt_to_local: Dict[int, int] = {gid: i for i, gid in enumerate(self.valid_genes)}
        self.local_to_scgpt: List[int] = self.valid_genes

        compact_tensor_path = self.codebook_dir / 'codebook_tensor_compact.pt'
        compact_map_path = self.codebook_dir / 'gene_to_compact_idx.json'
        full_tensor_path = self.codebook_dir / 'codebook_tensor.pt'

        self.use_compact = False
        self.codebook_tensor = None
        self.gene_to_compact: Optional[Dict[int, int]] = None

        if prefer_compact and compact_tensor_path.exists() and compact_map_path.exists():
            self.codebook_tensor = torch.load(compact_tensor_path, map_location='cpu', weights_only=True)
            with open(compact_map_path, 'r') as f:
                self.gene_to_compact = {int(k): int(v) for k, v in json.load(f).items()}
            self.use_compact = True
        elif full_tensor_path.exists():
            self.codebook_tensor = torch.load(full_tensor_path, map_location='cpu', weights_only=True)
            self.use_compact = False
        else:
            raise FileNotFoundError(
                f'No codebook tensor found in {self.codebook_dir}. '                f'Need compact(codebook_tensor_compact.pt) or full(codebook_tensor.pt).'
            )

        # Keep as float16 for parity with current model init path and lower memory.
        if self.codebook_tensor.dtype != torch.float16:
            self.codebook_tensor = self.codebook_tensor.to(torch.float16)

    def lookup(self, scgpt_ids: np.ndarray, cluster_indices: np.ndarray, return_token_ids: bool = True) -> CodebookLookupResult:
        if scgpt_ids.shape[0] != cluster_indices.shape[0]:
            raise ValueError(f'shape mismatch: scgpt_ids={scgpt_ids.shape}, cluster_indices={cluster_indices.shape}')

        L = int(scgpt_ids.shape[0])
        feats = np.zeros((L, 768), dtype=np.float16)
        valid_mask = np.zeros((L,), dtype=bool)
        token_ids = np.full((L,), -1, dtype=np.int64) if return_token_ids else None

        # Python loop is acceptable here because L is usually <=1200 per sample.
        for i in range(L):
            gid = int(scgpt_ids[i])
            cid = int(cluster_indices[i])
            k = self.nclusters.get(gid, 0)
            if k <= 0 or cid >= k:
                continue

            if self.use_compact:
                compact_idx = self.gene_to_compact.get(gid)
                if compact_idx is None:
                    continue
                feats[i] = self.codebook_tensor[compact_idx, cid].numpy()
            else:
                feats[i] = self.codebook_tensor[gid, cid].numpy()

            valid_mask[i] = True
            if return_token_ids:
                local_id = self.scgpt_to_local.get(gid, -1)
                if local_id >= 0:
                    token_ids[i] = local_id * self.clusters_per_gene + cid

        return CodebookLookupResult(features_768=feats, valid_mask=valid_mask, token_ids=token_ids)

    def build_gene_embedding_init(self) -> torch.Tensor:
        """
        Build model-compatible initialization matrix for gene_embed_tokens.
        Shape: [len(valid_genes)*clusters_per_gene + 1, 768]
        Last row is reserved for mask token (left as zeros).
        """
        vocab = len(self.valid_genes) * self.clusters_per_gene
        out = torch.zeros((vocab + 1, 768), dtype=torch.float16)

        for local_id, gid in enumerate(self.valid_genes):
            k = self.nclusters[gid]
            for cid in range(k):
                flat_id = local_id * self.clusters_per_gene + cid
                if self.use_compact:
                    compact_idx = self.gene_to_compact.get(gid)
                    if compact_idx is None:
                        continue
                    out[flat_id] = self.codebook_tensor[compact_idx, cid]
                else:
                    out[flat_id] = self.codebook_tensor[gid, cid]

        return out


class ClusterFeatureLoader:
    """O(1) random read loader for cluster feature LMDB shards."""

    def __init__(
        self,
        cluster_lmdb_dir: Union[str, Path],
        readahead: bool = False,
        max_readers: int = 2048,
        codebook_dir: Optional[Union[str, Path]] = None,
        clusters_per_gene: int = 64,
        prefer_compact_codebook: bool = True,
    ):
        self.cluster_lmdb_dir = Path(cluster_lmdb_dir)
        if not self.cluster_lmdb_dir.exists():
            raise FileNotFoundError(f'cluster_lmdb_dir not found: {self.cluster_lmdb_dir}')

        self.readahead = readahead
        self.max_readers = max_readers
        self._envs: Dict[str, lmdb.Environment] = {}

        self.codebook: Optional[GeneCodebookLookup] = None
        if codebook_dir is not None:
            self.codebook = GeneCodebookLookup(
                codebook_dir=codebook_dir,
                clusters_per_gene=clusters_per_gene,
                prefer_compact=prefer_compact_codebook,
            )

    def close(self):
        for env in self._envs.values():
            try:
                env.close()
            except Exception:
                pass
        self._envs.clear()

    def __del__(self):
        self.close()

    def _get_env(self, db_name: DbType) -> lmdb.Environment:
        norm = _normalize_db_name(db_name)
        if norm not in self._envs:
            db_path = self.cluster_lmdb_dir / norm
            if not db_path.exists():
                raise FileNotFoundError(f'cluster db not found: {db_path}')
            self._envs[norm] = lmdb.Environment(
                str(db_path),
                readonly=True,
                lock=False,
                readahead=self.readahead,
                meminit=False,
                max_readers=self.max_readers,
            )
        return self._envs[norm]

    def list_keys(self, db_name: DbType, exclude_meta: bool = True) -> List[bytes]:
        """Return all sample keys in one shard."""
        env = self._get_env(db_name)
        out: List[bytes] = []
        with env.begin(write=False) as txn:
            for key, _ in txn.cursor():
                if exclude_meta and key.startswith(b'-'):
                    continue
                out.append(bytes(key))
        return out

    def count_entries(self, db_name: DbType, exclude_meta: bool = True) -> int:
        env = self._get_env(db_name)
        if not exclude_meta:
            with env.begin(write=False) as txn:
                return int(txn.stat().get('entries', 0))
        return len(self.list_keys(db_name, exclude_meta=True))

    def get_raw(self, db_name: DbType, key: KeyType) -> Optional[bytes]:
        env = self._get_env(db_name)
        bkey = _normalize_key(key)
        with env.begin(write=False) as txn:
            return txn.get(bkey)

    def get(self, db_name: DbType, key: KeyType) -> Optional[ClusterFeatureRecord]:
        bkey = _normalize_key(key)
        raw = self.get_raw(db_name, bkey)
        if raw is None:
            return None
        return _decode_record(bkey, raw)

    def get_with_codebook(self, db_name: DbType, key: KeyType) -> Optional[Tuple[ClusterFeatureRecord, CodebookLookupResult]]:
        if self.codebook is None:
            raise RuntimeError('codebook is not configured. pass codebook_dir when creating loader.')
        rec = self.get(db_name, key)
        if rec is None:
            return None
        lookup = self.codebook.lookup(rec.scgpt_ids, rec.cluster_indices, return_token_ids=True)
        return rec, lookup

    def get_many(
        self,
        requests: Sequence[Tuple[DbType, KeyType]],
        num_workers: int = 0,
    ) -> List[Optional[ClusterFeatureRecord]]:
        if num_workers <= 0:
            return [self.get(db, key) for db, key in requests]

        task_list = [(_normalize_db_name(db), _normalize_key(key)) for db, key in requests]
        ctx = mp.get_context('spawn')
        with ctx.Pool(
            processes=num_workers,
            initializer=_worker_init,
            initargs=(str(self.cluster_lmdb_dir), self.readahead, self.max_readers),
        ) as pool:
            out = pool.map(_worker_get, task_list)
        return out

    def get_many_skip_missing(
        self,
        requests: Sequence[Tuple[DbType, KeyType]],
        num_workers: int = 0,
    ) -> Tuple[List[ClusterFeatureRecord], List[int]]:
        """
        批量读取并跳过不存在 key 的样本。

        Returns:
            kept_records: 仅包含命中的记录
            kept_indices: 每条命中记录在原 requests 中的下标
        """
        raw = self.get_many(requests, num_workers=num_workers)
        kept_records: List[ClusterFeatureRecord] = []
        kept_indices: List[int] = []
        for idx, rec in enumerate(raw):
            if rec is None:
                continue
            kept_records.append(rec)
            kept_indices.append(idx)
        return kept_records, kept_indices

    def filter_existing_requests(
        self,
        requests: Sequence[Tuple[DbType, KeyType]],
        num_workers: int = 0,
    ) -> Tuple[List[Tuple[DbType, KeyType]], List[int]]:
        """
        仅保留在特征库中存在 key 的请求，便于 dataset 侧直接跳过缺失样本。

        Returns:
            filtered_requests: 过滤后的请求
            kept_indices: 过滤后请求对应原 requests 的下标
        """
        kept_records, kept_indices = self.get_many_skip_missing(requests, num_workers=num_workers)
        _ = kept_records  # only need indices here
        filtered_requests = [requests[i] for i in kept_indices]
        return filtered_requests, kept_indices


# -------- multiprocessing worker state --------
_WORKER_DIR: Optional[Path] = None
_WORKER_READAHEAD: bool = False
_WORKER_MAX_READERS: int = 2048
_WORKER_ENVS: Dict[str, lmdb.Environment] = {}


def _worker_init(cluster_lmdb_dir: str, readahead: bool, max_readers: int):
    global _WORKER_DIR, _WORKER_READAHEAD, _WORKER_MAX_READERS, _WORKER_ENVS
    _WORKER_DIR = Path(cluster_lmdb_dir)
    _WORKER_READAHEAD = readahead
    _WORKER_MAX_READERS = max_readers
    _WORKER_ENVS = {}


def _worker_env(db_name: str) -> lmdb.Environment:
    global _WORKER_ENVS
    if db_name not in _WORKER_ENVS:
        db_path = _WORKER_DIR / db_name
        if not db_path.exists():
            raise FileNotFoundError(f'cluster db not found: {db_path}')
        _WORKER_ENVS[db_name] = lmdb.Environment(
            str(db_path),
            readonly=True,
            lock=False,
            readahead=_WORKER_READAHEAD,
            meminit=False,
            max_readers=_WORKER_MAX_READERS,
        )
    return _WORKER_ENVS[db_name]


def _worker_get(task: Tuple[str, bytes]) -> Optional[ClusterFeatureRecord]:
    db_name, key = task
    env = _worker_env(db_name)
    with env.begin(write=False) as txn:
        raw = txn.get(key)
    if raw is None:
        return None
    return _decode_record(key, raw)
